
public class LoopExcercise {

	public static void main(String[] args) {
		int i;      //loopval
		int N = 11; //valueend
		int a = 0;  //Addition Value
		   
		for(/* loopval (i) */i = 1;/* LoopVal (i) */ i < /* ValueEnd (N) */ N;/* loopVal (increment++) */ i++) {
			/* Addition --> */ a = a + i;
		}
		System.out.println("Total: " + a );
	}

}
